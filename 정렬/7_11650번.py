# 리스트 정렬 예시
lst = [(3, 4), (1, 2), (2, 1)]

# 두 번째 요소를 기준으로 정렬
sorted_lst = sorted(lst, key=lambda x: x[1])
print(sorted_lst)  # 출력: [(2, 1), (1, 2), (3, 4)]

# 리스트 자체를 정렬할 때는 sort() 메서드를 사용
lst.sort(key=lambda x: x[0])
print(lst)  # 출력: [(1, 2), (2, 1), (3, 4)]

